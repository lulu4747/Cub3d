#include "libft.h"

int	ft_isprint(int c)
{
	if ((c >= 32 && c <= 47) || ft_isalnum(c) == 1
	|| (c >= 58 && c <= 64) || (c >= 91 && c <= 96)
	|| (c >= 123 && c < 127))
		return (1);
	else
		return (0);
}
