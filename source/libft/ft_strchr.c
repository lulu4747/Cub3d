#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*p;

	i = 0;
	p = (char *)s;
	while (s[i])
	{
		if (*p == c)
			return (p);
		i++;
		p++;
	}
	if (c == 0)
		return (p);
	return (NULL);
}
